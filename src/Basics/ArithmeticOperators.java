package Basics;

public class ArithmeticOperators {
    public static void main(String[] args) {
        int x = 23;
        int y = 56;

        //Addition
        int sum = x + y;
        System.out.println(sum);

        //Subtraction
        int sub = x - y;
        System.out.println(sub);

        //Division
        int div = y / x;
        System.out.println(div);

        //Multiplication
        int mul = x * y;
        System.out.println(mul);

        //Modulus
        int mod = x % y;
        System.out.println(mod);
    }
}
