package Basics;

public class NamingConvention {
    public static void main(String[] args) {
        //camelCase
        String firstName = "Ibrar";
        String lastName = "Khan";
        System.out.println(firstName + lastName);

        //snake_case
        int your_number = 1234;
        System.out.println(your_number);

        //kebab-case
        //boolean is-ready = true;
        // but it's not a valid variable name in java
    }
}
