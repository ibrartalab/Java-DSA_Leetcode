package Basics;

public class RelationOperators {
    public static void main(String[] args) {
        int x = 76;
        int y = 45;

        boolean xGreater = x > y;
        boolean xLess = x < y;
        boolean xEqual = x == y;
        boolean xNotEqual = x != y;
        boolean xEqualGreater = x >= y;
        boolean xLessGreater = x <= y;

        System.out.println(x);
        System.out.println(y);
        System.out.println(xGreater);
        System.out.println(xLess);
        System.out.println(xEqual);
        System.out.println(xNotEqual);
        System.out.println(xEqualGreater);
        System.out.println(xLessGreater);
    }
}
